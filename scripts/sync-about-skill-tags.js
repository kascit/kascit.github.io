#!/usr/bin/env node
"use strict";

const fs = require("fs");
const path = require("path");

const ROOT = path.resolve(__dirname, "..");
const CONTENT_DIR = path.join(ROOT, "content");
const ABOUT_FILE = path.join(CONTENT_DIR, "about", "_index.md");
const OUTPUT_FILE = path.join(CONTENT_DIR, "about", "_skill-tags-seed.md");
const DATA_OUTPUT_FILE = path.join(ROOT, "data", "about-skill-tags.json");

function readText(filePath) {
  return fs.readFileSync(filePath, "utf8");
}

function walkMarkdownFiles(dirPath, out = []) {
  const entries = fs.readdirSync(dirPath, { withFileTypes: true });

  for (const entry of entries) {
    const fullPath = path.join(dirPath, entry.name);
    if (entry.isDirectory()) {
      walkMarkdownFiles(fullPath, out);
      continue;
    }

    if (!entry.isFile()) continue;
    if (!entry.name.endsWith(".md")) continue;

    out.push(fullPath);
  }

  return out;
}

function uniquePreserveOrder(values) {
  const out = [];
  const seen = new Set();

  for (const value of values) {
    const trimmed = String(value || "").trim();
    if (!trimmed) continue;

    const key = trimmed.toLowerCase();
    if (seen.has(key)) continue;

    seen.add(key);
    out.push(trimmed);
  }

  return out;
}

function extractAboutSkillTags(raw) {
  const tags = [];
  const tagChipRegex = /\{\{\s*tag_chip\(\s*name\s*=\s*"([^"]+)"[^)]*\)\s*\}\}/g;

  let match;
  while ((match = tagChipRegex.exec(raw)) !== null) {
    tags.push(match[1]);
  }

  return uniquePreserveOrder(tags);
}

function extractFrontMatter(raw) {
  const match = raw.match(/^\+\+\+\s*\n([\s\S]*?)\n\+\+\+/);
  return match ? match[1] : "";
}

function extractTagsFromFrontMatter(frontMatter) {
  const tags = [];
  const tagsArrayRegex = /\btags\s*=\s*\[([\s\S]*?)\]/g;

  let arrayMatch;
  while ((arrayMatch = tagsArrayRegex.exec(frontMatter)) !== null) {
    const arrayBody = arrayMatch[1];
    const itemRegex = /"((?:[^"\\]|\\.)*)"/g;

    let itemMatch;
    while ((itemMatch = itemRegex.exec(arrayBody)) !== null) {
      const value = itemMatch[1].replace(/\\"/g, '"').replace(/\\\\/g, "\\");
      tags.push(value);
    }
  }

  return uniquePreserveOrder(tags);
}

function collectExistingTags() {
  const allFiles = walkMarkdownFiles(CONTENT_DIR);
  const tags = [];

  for (const filePath of allFiles) {
    if (path.resolve(filePath) === path.resolve(OUTPUT_FILE)) {
      continue;
    }

    const raw = readText(filePath);
    const frontMatter = extractFrontMatter(raw);
    if (!frontMatter) continue;

    tags.push(...extractTagsFromFrontMatter(frontMatter));
  }

  return uniquePreserveOrder(tags);
}

function tomlString(value) {
  return `"${String(value).replace(/\\/g, "\\\\").replace(/"/g, '\\"')}"`;
}

function tomlArray(values) {
  if (!values.length) return "[]";
  return `[${values.map((value) => tomlString(value)).join(", ")}]`;
}

function toSlug(value) {
  return String(value || "")
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function renderSkillDataFile(aboutSkillTags) {
  const terms = aboutSkillTags.map((name) => ({
    name,
    slug: toSlug(name),
    lower: String(name).toLowerCase(),
  }));

  return JSON.stringify(
    {
      skill_terms: terms,
      skill_terms_lc: terms.map((entry) => entry.lower),
    },
    null,
    2
  ) + "\n";
}

function renderSeedFile(tags) {
  return [
    "+++",
    'title = "About Skill Taxonomy Seed"',
    'description = "Generated taxonomy seed page for About skill chips."',
    'date = "2026-01-01"',
    'template = "page.html"',
    "render = false",
    "in_search_index = false",
    "[extra]",
    "generated = true",
    "[taxonomies]",
    `tags = ${tomlArray(tags)}`,
    "+++",
    "",
    "This page is generated by scripts/sync-about-skill-tags.js.",
    "Do not edit manually.",
    "",
  ].join("\n");
}

function main() {
  const aboutRaw = readText(ABOUT_FILE);
  const aboutSkillTags = extractAboutSkillTags(aboutRaw);
  const existingTags = collectExistingTags();

  const existingSet = new Set(existingTags.map((tag) => tag.toLowerCase()));
  const missingTags = aboutSkillTags.filter((tag) => !existingSet.has(tag.toLowerCase()));

  fs.writeFileSync(OUTPUT_FILE, renderSeedFile(missingTags), "utf8");
  fs.writeFileSync(DATA_OUTPUT_FILE, renderSkillDataFile(aboutSkillTags), "utf8");

  console.log(
    `About skill tags: ${aboutSkillTags.length}, existing tags: ${existingTags.length}, seeded missing tags: ${missingTags.length}`
  );

  if (missingTags.length > 0) {
    console.log(`Seeded tags: ${missingTags.join(", ")}`);
  }
}

main();
